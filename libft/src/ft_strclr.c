#include "libft.h"

void	ft_strclr(char *s)
{
	size_t	i;

	if (!(s))
		return ;
	i = ft_strlen((const char*)s);
	i--;
	while ((int)i >= 0)
	{
		s[i] = '\0';
		i--;
	}
}
